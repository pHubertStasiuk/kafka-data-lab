    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE shop;

-- =============================================================================
-- TABLES
-- =============================================================================

CREATE TABLE customers (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    email         VARCHAR(255)    NOT NULL UNIQUE,
    first_name    VARCHAR(100)    NOT NULL,
    last_name     VARCHAR(100)    NOT NULL,
    country       CHAR(2)         NOT NULL COMMENT 'ISO 3166-1 alpha-2',
    tier          ENUM('bronze','silver','gold','platinum') NOT NULL DEFAULT 'bronze'
                  COMMENT 'Loyalty tier — drives discount and priority routing',
    created_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_country (country),
    INDEX idx_tier (tier)
) COMMENT 'Customer master data — CDC source for customers KTable in Kafka Streams';

CREATE TABLE products (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    name          VARCHAR(255)    NOT NULL,
    category      VARCHAR(100)    NOT NULL,
    price         DECIMAL(10,2)   NOT NULL,
    stock_qty     INT             NOT NULL DEFAULT 0,
    is_active     BOOLEAN         NOT NULL DEFAULT TRUE,
    created_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_category (category),
    INDEX idx_active (is_active)
) COMMENT 'Product catalog — also updated via MinIO CSV pipeline (Pipeline 2)';

CREATE TABLE orders (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    customer_id   INT             NOT NULL,
    status        ENUM('pending','paid','shipped','delivered','cancelled','refunded')
                  NOT NULL DEFAULT 'pending',
    total_amount  DECIMAL(12,2)   NOT NULL,
    discount_code VARCHAR(50)     NULL COMMENT 'Added in schema v2 — backward compatible',
    created_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (customer_id) REFERENCES customers(id),
    INDEX idx_customer (customer_id),
    INDEX idx_status (status),
    INDEX idx_created (created_at)
) COMMENT 'Order header — primary CDC stream for Pipeline 1';

CREATE TABLE order_items (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    order_id      INT             NOT NULL,
    product_id    INT             NOT NULL,
    quantity      INT             NOT NULL DEFAULT 1,
    unit_price    DECIMAL(10,2)   NOT NULL COMMENT 'Price at time of order — snapshot, not FK to products.price',
    FOREIGN KEY (order_id)   REFERENCES orders(id),
    FOREIGN KEY (product_id) REFERENCES products(id),
    INDEX idx_order (order_id),
    INDEX idx_product (product_id)
) COMMENT 'Order line items — joined with orders in Kafka Streams';

